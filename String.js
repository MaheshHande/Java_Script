// String in JS And Methods 
const Name = "Mahesh Hande";
console.log(Name);




// String As a Object :-
// We Can Define String Object by using new 'Key' word 
let y = new String("Iron Man");





// Methods OF String:-
// 1.Length Function
console.log("String Length of Mahesh: "+Name.length);
console.log("String Length of Iron Man is: "+y.length);



// 2.charAt()
console.log(Name.charAt(2));
console.log(y.charAt(3));



// 3.charCodeAt()
console.log(Name.charCodeAt(0));



// 4.at()--> Gives The String AT the Index Position
console.log(Name.at(2));



// 5.slice()

console.log(Name.slice(0,6));

console.log(Name.slice(0));

console.log(Name.slice(6));

console.log(Name.slice(-12));






